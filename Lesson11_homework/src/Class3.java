public class Class3 {
    int x, y, z;

    Class3 () {
        this.x = 5;
        this.y = 10;
        this.z = 2;
    }

    Class3 (int x) {
        this.x = x;
    }
    Class3 (int x, int y) {
        this.x = x;
        this.y = y;
    }
    Class3 (int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
